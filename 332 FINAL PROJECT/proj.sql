CREATE TABLE PROFESSOR(
    SSN NUMERIC(9) NOT NULL,
    FNAME VARCHAR(20),
    LNAME VARCHAR(20),
    STREET_ADDRESS VARCHAR(30),
    CITY VARCHAR(20),
    ST CHAR(2),
    ZIP_CODE NUMERIC(5),
    AREA_CODE NUMERIC(3),
    P_NUM NUMERIC(7),
    SEX CHAR(1),
    TITLE VARCHAR(20),
    SALARY VARCHAR(10),
    PRIMARY KEY(SSN)
);

CREATE TABLE DEPARTMENT(
    DEPT_NUM CHAR(4) NOT NULL,
    DEPT_NAME VARCHAR(30),
    DEPT_PHONE NUMERIC(10),
    DEPT_LOCATION VARCHAR(20),
    CHAIR_SSN NUMERIC(9) NOT NULL,
    PRIMARY KEY(DEPT_NUM),
    FOREIGN KEY(CHAIR_SSN) REFERENCES PROFESSOR(SSN)
);

CREATE TABLE COURSE(
    COURSE_NUM VARCHAR(10) NOT NULL,
    TITLE CHAR(50),
    TEXTBOOK VARCHAR(100),
    UNITS NUMERIC(1),
    D_NUM CHAR(4) NOT NULL,
    PRIMARY KEY(COURSE_NUM),
    FOREIGN KEY(D_NUM) REFERENCES DEPARTMENT(DEPT_NUM)
	--COURSE_NUM = course number
);

CREATE TABLE STUDENT(
    ID NUMERIC(9) NOT NULL,
    ADDR VARCHAR(100),
    F_NAME VARCHAR(20),
    L_NAME VARCHAR(20),
    S_PHONE NUMERIC(10),
    SD_NUM CHAR(4) NOT NULL,
    PRIMARY KEY(ID),
    FOREIGN KEY(SD_NUM) REFERENCES DEPARTMENT(DEPT_NUM)
);


CREATE TABLE COURSE_SECTION(
    SECTION_NUM NUMERIC(10) NOT NULL,
    COURSE_NO VARCHAR(10) NOT NULL,
    CLASSROOM VARCHAR(5),
    MEETING_DAYS VARCHAR(10),
    START_TIME TIME,
    END_TIME TIME,
    SEAT_NUM NUMERIC(2),
    PROF_SN NUMERIC(9) NOT NULL,
    PRIMARY KEY(SECTION_NUM, COURSE_NO),
    FOREIGN KEY(COURSE_NO) REFERENCES COURSE(COURSE_NUM),
    FOREIGN KEY(PROF_SN) REFERENCES PROFESSOR(SSN)
	--SECTION_NUM = course number
	--COURSE_NO = section number
);

CREATE TABLE MINOR(
    DEP_NUM CHAR(4) NOT NULL,
    STUD_ID NUMERIC(9) NOT NULL,
    PRIMARY KEY(DEP_NUM, STUD_ID),
    FOREIGN KEY(DEP_NUM) REFERENCES DEPARTMENT(DEPT_NUM),
    FOREIGN KEY(STUD_ID) REFERENCES STUDENT(ID)
);

CREATE TABLE ENROLLMENT(
    STUDENT_CWID NUMERIC(9) NOT NULL,
    SEC_NUM NUMERIC(10) NOT NULL,
    C_NUM VARCHAR(10) NOT NULL,
    GRADE CHAR(2),
    PRIMARY KEY(STUDENT_CWID, SEC_NUM, C_NUM),
    FOREIGN KEY(STUDENT_CWID) REFERENCES STUDENT(ID),
    FOREIGN KEY(SEC_NUM) REFERENCES COURSE_SECTION(SECTION_NUM),
    FOREIGN KEY(C_NUM) REFERENCES COURSE_SECTION(COURSE_NO)
	--COURSE_NUM = course number
	--C_NUM = section number
);

CREATE TABLE PREREQUISITE(
    PREREQ VARCHAR(20) NOT NULL,
    CO_NUM VARCHAR(10) NOT NULL,
    PRIMARY KEY(CO_NUM, PREREQ),
    FOREIGN KEY(CO_NUM) REFERENCES COURSE(COURSE_NUM)
);

CREATE TABLE PROF_DEGREE(
    PROFESSOR_SSN NUMERIC(9) NOT NULL,
    DEGREE CHAR(15) NOT NULL,
    PRIMARY KEY (PROFESSOR_SSN),
    FOREIGN KEY (PROFESSOR_SSN) REFERENCES PROFESSOR(SSN)
);
